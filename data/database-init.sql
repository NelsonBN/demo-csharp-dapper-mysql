CREATE SCHEMA IF NOT EXISTS `demo_db` DEFAULT CHARACTER SET utf8mb4 ;

USE `demo_db` ;

CREATE TABLE IF NOT EXISTS `continent` (
  `continent_id` INT(4) UNSIGNED NOT NULL,
  `english_name` VARCHAR(150) NOT NULL,
  PRIMARY KEY (`continent_id`),
  UNIQUE INDEX `uq__continent__english_name` (`english_name` ASC))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4;


CREATE TABLE IF NOT EXISTS `country` (
  `iso2` CHAR(2) NOT NULL,
  `iso3` CHAR(3) NOT NULL,
  `iso_numeric` INT(4) UNSIGNED NOT NULL,
  `english_name` VARCHAR(150) NOT NULL,
  `native_name` VARCHAR(150) NULL DEFAULT NULL,
  `nationality_name` VARCHAR(150) NULL DEFAULT NULL,
  `continent_id` INT(4) UNSIGNED NOT NULL,
  `capital` VARCHAR(150) NULL DEFAULT NULL,
  `currency_code` VARCHAR(3) NULL DEFAULT NULL,
  `domain` VARCHAR(2) NULL DEFAULT NULL,
  UNIQUE INDEX `uq__country__iso_numeric` (`iso_numeric` ASC),
  UNIQUE INDEX `uq__country__iso3` (`iso3` ASC),
  UNIQUE INDEX `uq__country__english_name` (`english_name` ASC),
  INDEX `fk__country__continent_id` (`continent_id` ASC),
  UNIQUE INDEX `uq__country__native_name` (`native_name` ASC),
  PRIMARY KEY (`iso2`),
  CONSTRAINT `fk__country__continent_id`
    FOREIGN KEY (`continent_id`)
    REFERENCES `continent` (`continent_id`)
    ON DELETE NO ACTION
    ON UPDATE NO ACTION)
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4;


CREATE TABLE IF NOT EXISTS `person` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
  `name` VARCHAR(50) NOT NULL,
  `age` INT(11) UNSIGNED NOT NULL,
  PRIMARY KEY (`id`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4 ;



CREATE TABLE IF NOT EXISTS `product` (
  `id` INT(11) UNSIGNED NOT NULL AUTO_INCREMENT,
  `description` VARCHAR(50) NOT NULL,
  `quantity` INT(11) UNSIGNED NOT NULL,
  PRIMARY KEY (`id`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4 ;

CREATE TABLE IF NOT EXISTS `event_sourcing` (
  `id` INT(1) UNSIGNED NOT NULL AUTO_INCREMENT,
  `log` VARCHAR(250) NOT NULL,
  `datetime` DATETIME NOT NULL,
  PRIMARY KEY (`id`))
ENGINE = InnoDB
DEFAULT CHARACTER SET = utf8mb4 ;



